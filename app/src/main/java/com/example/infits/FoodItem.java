package com.example.infits;

public class FoodItem {
    public String name, calorie, protein, fibre, carb, fat;

    public FoodItem(String name, String calorie, String protein, String fibre, String carb, String fat) {
        this.name = name;
        this.calorie = calorie;
        this.protein = protein;
        this.fibre = fibre;
        this.carb = carb;
        this.fat = fat;
    }
}
