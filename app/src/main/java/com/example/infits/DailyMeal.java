package com.example.infits;

public class DailyMeal {
    public String name, calorie, protein, fibre, carb, fat, time, goal;

    public DailyMeal(String name, String calorie, String protein, String fibre, String carb, String fat, String time, String goal) {
        this.name = name;
        this.calorie = calorie;
        this.protein = protein;
        this.fibre = fibre;
        this.carb = carb;
        this.fat = fat;
        this.time = time;
        this.goal = goal;
    }
}
